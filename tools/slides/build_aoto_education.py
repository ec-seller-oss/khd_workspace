NAVY = "2E4A7A"; CREAM = "F9F6EF"; YEL = "FFF2CC"; GRY = "EFEFEF"; PINK = "FCE4EC"; BLU = "CFE2F3"; GRN = "D9EAD3"; ORG = "FCE5CD"
thin = Side(style="thin", color="BBBBBB")
BORDER = Border(left=thin, right=thin, top=thin, bottom=thin)
wb = Workbook()

def H(sh, row, n, fill=NAVY, color="FFFFFF"):
    for c in range(1, n + 1):
        x = sh.cell(row=row, column=c)
        x.font = Font(name=FONT, bold=True, color=color, size=10)
        x.fill = PatternFill("solid", start_color=fill)
        x.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        x.border = BORDER

def rows(sh, start, data, widths=None, size=10):
    for i, r in enumerate(data):
        for j, v in enumerate(r):
            x = sh.cell(row=start + i, column=j + 1, value=v)
            x.font = Font(name=FONT, size=size)
            x.alignment = Alignment(vertical="center", wrap_text=True)
            x.border = BORDER
    if widths:
        for k, w in enumerate(widths):
            sh.column_dimensions[get_column_letter(k + 1)].width = w

def T(sh, text, n):
    sh.cell(row=1, column=1, value=text).font = Font(name=FONT, bold=True, size=14, color=NAVY)
    sh.merge_cells(start_row=1, start_column=1, end_row=1, end_column=n)

def sec(sh, row, text):
    sh.cell(row=row, column=1, value=text).font = Font(name=FONT, bold=True, size=11, color=NAVY)

